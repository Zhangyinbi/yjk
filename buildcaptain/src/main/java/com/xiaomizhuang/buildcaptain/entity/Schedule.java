/**
 * 描述: 设置排期 列表bean
 * --------------------------------------------
 * 工程:
 * #0000     mwy     创建日期: 2016-09-08 13:46
 */
public class Schedule {

	/**
	 * id : 1
	 * name : 交底/拆旧
	 * step : 1
	 */

	private String id;
	private String name;
	private String step;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getStep() {
		return step;
	}

	public void setStep(String step) {
		this.step = step;
	}
}
