public class MoneyMatters {


	private String msg_id;
	private String msg;
	private String payable_amount;
	private String time_of_payment;
	private String status;
	private int count;
	private String create_time;
	private String messang;

	public String getMsg_id() {
		return msg_id;
	}

	public void setMsg_id(String msg_id) {
		this.msg_id = msg_id;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public String getPayable_amount() {
		return payable_amount;
	}

	public void setPayable_amount(String payable_amount) {
		this.payable_amount = payable_amount;
	}

	public String getTime_of_payment() {
		return time_of_payment;
	}

	public void setTime_of_payment(String time_of_payment) {
		this.time_of_payment = time_of_payment;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public String getCreate_time() {
		return create_time;
	}

	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}

	public String getMessang() {
		return messang;
	}

	public void setMessang(String messang) {
		this.messang = messang;
	}
}
