package com.example.yangbang.miowner.entity;

import java.io.Serializable;

/**
 * Created by mwy on 2016/7/1.
 */
public class SincePurchase implements Serializable {

	private int class_id;
	private String material_name;
	private String project_name;
	private String remind_day;
	private int status;
	private int count;
	private String create_time;
	private String message;

	public int getClass_id() {
		return class_id;
	}

	public void setClass_id(int class_id) {
		this.class_id = class_id;
	}

	public String getMaterial_name() {
		return material_name;
	}

	public void setMaterial_name(String material_name) {
		this.material_name = material_name;
	}

	public String getProject_name() {
		return project_name;
	}

	public void setProject_name(String project_name) {
		this.project_name = project_name;
	}

	public String getRemind_day() {
		return remind_day;
	}

	public void setRemind_day(String remind_day) {
		this.remind_day = remind_day;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public String getCreate_time() {
		return create_time;
	}

	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}

	public String getMessage() {
		return message;
	}

	public void setMessage(String message) {
		this.message = message;
	}
}
